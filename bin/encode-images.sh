#!/usr/bin/env bash
set -euo pipefail
cd "$(dirname "$0")/.."
out="src/shared.liquid"
{
  echo '{%- comment -%} GENERATED by bin/encode-images.sh -- do not edit by hand {%- endcomment -%}'
  printf '{%% assign hog_images = "'
  for i in 1 2 3 4 5 6; do
    [ "$i" -gt 1 ] && printf '|||'
    printf 'data:image/png;base64,'
    base64 -i "src/assets/igel-$i.png" | tr -d '\n'
  done
  printf '" | split: "|||" -%%}\n'
} > "$out"
echo "wrote $out ($(wc -c < "$out") bytes)"
